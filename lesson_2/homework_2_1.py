# 2. Основные арифметические операции
# ================================================
# Создай две переменные num1 и num2, присвой им любые числа.
num1, num2 = 100, 200
# Выведи результаты следующих операций:
# сложение num1 + num2
print(num1 + num2)
# вычитание num1 - num2
print(num1 - num2)
# умножение num1 * num2
print(num1 * num2)
# деление num1 / num2
print(num1 / num2)
# целочисленное деление num1 // num2
print(num1 // num2)
# остаток от деления num1 % num2
print(num1 % num2)
# возведение в степень num1 ** num2
print(num1 ** num2)
